package animal;

public class Caca {
	
	private int peso;
	private String color;
	private boolean consistencia;
	
	
	public Caca(int peso, String color, boolean consistencia) {
		this.peso=peso;
		this.color=color;
		this.consistencia=consistencia;
	}


	public int getPeso() {
		return peso;
	}


	public String getColor() {
		return color;
	}


	public boolean isConsistencia() {
		return consistencia;
	}
	
	

}
